                <h3 class="text-center">Bem vindo ao colégio NAIARA BAVARESCO DE SOUZA</h3>
                <br>
                <p>No ano de 2008, dez municípios do Paraná foram contemplados para receber uma unidade de Centro Estadual de Educação Profissional – CEEP.  Já haviam cinco unidades no estado e a partir de 2008 a previsão é que mais 10 (dez) seriam construídos. Dentre os municípios selecionados, Laranjeiras do Sul foi contemplada com o objetivo de atender uma demanda regional, visto que o Município está mais centralizado e poderia atender os demais da região.  O município de Laranjeiras do Sul é Sede do Conselho de Desenvolvimento do território Cantuquiriguaçu CONDETC.  

O Território da Cantuquiriguaçu, reflete com precisão a essência da Associação de Municípios da Região Centro – Oeste do Paraná, que chamamos de Cantu. A entidade é o elo da união de 20 municípios, que juntos lutam por mais desenvolvimento para suas cidades. 

 Diante disso, pontua-se que o CEEP ficará localizado em uma área com grande demanda e várias especificidades, como: o maior assentamento da América Latina: Ireno Alves dos Santos e maior Terra Indígena do Paraná: Terra Indígena Rio das Cobras. 

 Diante desse contexto, objetivando demonstrar a necessidade de uma unidade escolar de Educação Profissional foi realizado um estudo de demanda da região referente a alunos regressos do Ensino Fundamental, com a possibilidade de matrícula nos cursos técnicos na forma integral e de alunos egressos do Ensino Médio com possibilidade de matrícula para cursos técnicos na forma subsequente. 

 A partir dessa definição, foram realizadas várias reuniões com Entidades do Município juntamente com as Secretarias Municipais de Educação dos 10 municípios jurisdicionados ao NRE de Laranjeiras do Sul, no intuito de propor e discutir a demanda e a oferta de cursos, bem como suas especificidades a serem ofertadas. 

          Após o estudo dos cursos, do perfil dos estudantes, demanda de profissionais nas áreas específicas, as possíveis parcerias e locais de estágio optou-se pelos seguintes cursos: Técnico em Contabilidade (subsequente e integrado), Técnico em Edificações (subsequente e integrado), Técnico em Agronegócio (subsequente e integrado) e Técnico em Enfermagem (subsequente). 

 Enquanto as discussões aconteciam, dava-se início à construção da estrutura física, no ano de 2011. Mas no dia 29 de outubro de 2013 houve a suspensão das obras porque segundo o Departamento Nacional de Infraestrutura de Transporte – DNIT a construção estava sobre a faixa de domínio, ocasionando atrasos na construção. 

 Em maio de 2016 é nomeada a primeira diretora do Centro Estadual de Educação Profissional Professora Naiana Babaresco de Souza, a Professora Eliane Maria da Silva, formada em Ciências com Habilitação em Matemática, Pós-Graduada em Matemática e Física, Especialização em Matemática na Prática para o Ensino Médio. A Nomeação foi realizada pela chefe do Núcleo Regional de Educação a Senhora Eliza Regina Gemelli da Silva com vinte horas no período da manhã. Com muita determinação a diretora Eliane Maria da Silva aceitou o grande desafio.  

 Nesse período a diretora e quatro Agentes Educacionais I, iniciaram as atividades  começando pela  limpeza do local, até a  distribuição de todo o mobiliário que estava tudo em um dos laboratórios, foram sessenta dias de muito trabalho e esforço por toda a equipe, ou seja, a direção e quatro agentes.  

 Em agosto de 2016 início o trabalho com alunos no período noturno com os cursos Técnico em Contabilidade, duas turmas e Técnico em Agronegócio, também com duas turmas na modalidade subsequente. Então assim entra em funcionamento 14º Centro Estadual de Educação Profissional do Estado do Paraná. Somente em setembro de 2016 a direção passou do turno da manhã para noite. Em fevereiro de 2017 iniciou o primeiro curso Técnico em Agronegócio Integrado no período da manhã e através da resolução 00460/17 e a diretora foi nomeada com quarenta horas no período manhã e noite.   

 O Centro Estadual de Educação Profissional Professora Naiana Babaresco de Souza tem capacidade de atendimento a 1200 alunos, a área construída é de 6. 495,56 m², e a área livre é de 8.458,59m2 totalizando uma área de 14.354,15 m². 

 A primeira nomenclatura definida em conjunto com o NRE foi “Centro Estadual de Educação Profissional de Laranjeiras do Sul”. Quando Logo em seguida optou-se em homenagear alguém do município e que tivesse vínculo com a educação. Então foi sugerido em reunião com a então Chefe do NRE Eliza Regina Gemelli da Silva, e o nome escolhido foi o da servidora pública Professora Naiana Babaresco de Souza a qual lecionou a disciplina de Educação Física em diversas instituições de ensino deste Núcleo Regional de Educação, demonstrando ser uma profissional competente e comprometida com a Educação. Além destas virtudes, demonstrou determinação ao enfrentar no auge de sua juventude o câncer. A demanda de alunos será formada, em sua maioria por alunos jovens. Portanto, esta escolha tem um referencial jovem e serve de homenagem à servidora. 

Atualmente na modalidade Subsequente ofertamos 3 Cursos:  Técnico em Administração, Técnico em Enfermagem e Técnico em Informática. Na modalidade Integrado, ou seja, Ensino Médio Profissionalizante ofertamos: Técnico em Agricultura, Técnico em Administração, Técnico em Desenvolvimento de Sistemas e Formação de Docentes. 
                </p>


